public class MonthExpense {
        static String item_name;
        static Boolean is_expense;
        static Integer quantity;
        static Integer sum_of_one;

        public MonthExpense(String item_name, Boolean is_expense, Integer quantity, Integer sum_of_one){
            this.item_name = item_name;
            this.is_expense = is_expense;
            this.quantity = quantity;
            this.sum_of_one = sum_of_one;
        }




}
